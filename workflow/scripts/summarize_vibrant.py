def create_metabolism_plot(
    vibrant_output_dirs: List[str], threads: Union[int, None]
) -> Tuple[pd.DataFrame, matplotlib.figure.Figure]:
    with mp.Pool(threads) as p:
        dataframes = p.map(matabolism_pathway_amg_df, vibrant_output_dirs)

    merged_df = reduce(merge_metabolism_frames, dataframes).sort_values(
        ["Metabolism", "Pathway", "AMG KO"], ascending=False
    )

    metabolism_label_df = metabolism_df(merged_df, "AMG KO")

    merged_df = merged_df.merge(
        metabolism_label_df["color"],
        left_on="Metabolism",
        right_index=True,
    )
    merged_df["y_label"] = merged_df["AMG KO"] + " | " + merged_df["Pathway"]
    fig = all_metabolism_map(merged_df, metabolism_label_df)

    return merged_df, fig


def main() -> None:
    args = parse_arguments()

    if args.multiple_directories:
        output_directories = find_output_directories(args.input_dir)

        with mp.Pool(args.threads) as p:
            outputs = p.map(parse_single_directory, output_directories)

        multiple_df = pd.DataFrame(dict(outputs))
        multiple_df.to_csv(args.output, sep="\t")

    else:
        output_directories = [args.input_dir]
        parse_single_directory(args.input_dir, args.output)

    amg_plot_path = args.input_dir + "_amg_plot.pdf"
    amg_plot = create_amg_plot(output_directories, args.threads)
    amg_plot.savefig(amg_plot_path, bbox_inches="tight")

    metabolism_plot_path = args.input_dir + "_metabolism_plot.pdf"
    metabolism_tsv_path = args.input_dir + "_metabolism.tsv"
    metabolism_df, metabolism_plot = create_metabolism_plot(
        output_directories, args.threads
    )
    metabolism_plot.savefig(metabolism_plot_path, bbox_inches="tight")
    metabolism_df.to_csv(metabolism_tsv_path, sep="\t", index=False)


if __name__ == "__main__":
    main()
