def parse_arguments() -> argparse.Namespace:
    """A function for parsing the command line arguments outside of the main function.
    Signals conflicts among command line options."""

    parser = argparse.ArgumentParser(
        description="""A script for the parsing of output generated by VIBRANT""",
    )
    parser.add_argument(
        "-i",
        "--input_dir",
        help="""An output directory produced by VIBRANT, or a directory that contains multiple VIBRANT output directories""",
        required=True,
    )

    parser.add_argument(
        "-o", "--output", help="""Summary file produced by this script"""
    )

    parser.add_argument(
        "-f",
        "--force",
        action="store_true",
        help="""Overwrite output file if present""",
    )

    parser.add_argument(
        "-m",
        "--multiple_directories",
        action="store_true",
        help="""Select this option if the input directory contains more than one VIBRANT output directory""",
    )

    parser.add_argument(
        "-t",
        "--threads",
        type=int,
        default=None,
        help="""Number of processors used when working on multiple directories.
                        Defaults to the number of cpu cores.""",
    )

    args = parser.parse_args()

    if not path.isdir(args.input_dir):
        raise Exception(
            "Input argument {} is not a directory".format(args.input_dir)
        )

    args.input_dir = path.normpath(args.input_dir)

    if not args.multiple_directories and not path.basename(
        args.input_dir
    ).startswith("VIBRANT_"):
        print(
            "Warning: input directory {} does not start with the prefix VIBRANT_".format(
                args.input_dir
            )
        )

    if not args.multiple_directories and args.threads is not None:
        print(
            """Warning: the --threads option has been selected with {} cores, but this script has been launched on a single directory.
        If you plan to analyze multiple directories, please select the -m / --multiple_directories option""".format(
                args.threads
            )
        )

    if args.output is None:
        if args.multiple_directories:
            args.output = args.input_dir + "_multiple_summary.txt"
        else:
            args.output = args.input_dir + "_summary.txt"

    if path.isfile(args.output) and not args.force:
        raise Exception(
            """Output file {} already exists.
                        Specify -f or --force to overwrite it""".format(
                args.output
            )
        )

    return args


def find_vibrant_log(vibrant_output_dir: str) -> str:
    """A function to find the run log of the VIBRANT output directory.
    Ensures there is one and only one run log file."""

    glob_files = glob(path.join(vibrant_output_dir, "VIBRANT_log_run*.log"))

    if len(glob_files) < 1:
        raise Exception(
            "No log file was found in the VIBRANT output directory {}".format(
                vibrant_output_dir
            )
        )

    if len(glob_files) > 1:
        raise Exception(
            "More than one log file was found in the VIBRANT output directory {}".format(
                vibrant_output_dir
            )
        )

    return glob_files[0]


def parse_vibrant_log(vibrant_log_file: str) -> dict:
    """Parse the run log file to retrieve info about the number of scaffolds"""

    dct = {
        "scaffolds read": None,
        "scaffolds with minimum requirements": None,
        "putative phages": None,
    }

    with open(vibrant_log_file) as f:
        for line in f:
            match_scaffolds_read = re.match(
                r"([0-9]+) scaffolds were read in", line
            )
            match_scaffolds_min_req = re.match(
                r"([0-9]+) scaffolds met minimum requirements", line
            )
            match_putative_phages = re.match(r"([0-9]+) putative phages", line)

            if match_scaffolds_read:
                dct["scaffolds read"] = int(match_scaffolds_read.group(1))

            if match_scaffolds_min_req:
                dct["scaffolds with minimum requirements"] = int(
                    match_scaffolds_min_req.group(1)
                )

            if match_putative_phages:
                dct["putative phages"] = int(match_putative_phages.group(1))

    return dct


def find_types_quality_file(vibrant_output_dir: str) -> str:
    glob_files = glob(
        path.join(
            vibrant_output_dir,
            "VIBRANT_results*",
            "VIBRANT_genome_quality_*.tsv",
        )
    )

    if len(glob_files) < 1:
        raise Exception(
            """VIBRANT_gene_quality_ file was not found in directory {}.
                        It is supposed to be in the VIBRANT_results subdirectory""".format(
                vibrant_output_dir
            )
        )

    if len(glob_files) > 1:
        raise Exception(
            """More than one VIBRANT_gene_quality_ file was found in the VIBRANT output directory {}""".format(
                vibrant_output_dir
            )
        )

    return glob_files[0]


def parse_types_quality(types_quality_file) -> dict:
    dct = {
        "fragment": 0,
        "low quality draft": 0,
        "medium quality draft": 0,
        "high quality draft": 0,
        "complete circular": 0,
        "lytic": 0,
        "lysogenic": 0,
    }

    df = pd.read_table(types_quality_file)
    dct.update(df.groupby("type").count()["scaffold"].to_dict())
    dct.update(df.groupby("Quality").count()["scaffold"].to_dict())

    return dct


